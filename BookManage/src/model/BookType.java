package model;

public class BookType {
	
	private Integer id;
	private String tname;
	private String tdes;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public String getTdes() {
		return tdes;
	}
	public void setTdes(String tdes) {
		this.tdes = tdes;
	}
	
	
}
